package org.prasad;

public class BusinessLogic
{
    public int add(int a, int b)
    {
        return a+b;
    }

    public int subtract(int a, int b)
    {
        return a-b;
    }

    public int multiply(int a, int b)
    {
        return a*b;
    }

    public int division(int a, int b)
    {
        return a/b;
    }
}
